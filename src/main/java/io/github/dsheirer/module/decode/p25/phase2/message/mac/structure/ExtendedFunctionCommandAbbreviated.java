package io.github.dsheirer.module.decode.p25.phase2.message.mac.structure;

import io.github.dsheirer.bits.CorrectedBinaryMessage;
import io.github.dsheirer.bits.IntField;
import io.github.dsheirer.identifier.Identifier;
import io.github.dsheirer.module.decode.p25.identifier.radio.APCO25RadioIdentifier;
import io.github.dsheirer.module.decode.p25.reference.ExtendedFunction;
import java.util.Collections;
import java.util.List;

/**
 * Extended function command abbreviated
 */
public class ExtendedFunctionCommandAbbreviated extends MacStructure
{
    private static final IntField FUNCTION = IntField.length16(OCTET_2_BIT_8);
    private static final IntField ARGUMENTS = IntField.length24(OCTET_4_BIT_24);
    private static final IntField TARGET_ADDRESS = IntField.length24(OCTET_7_BIT_48);
    private ExtendedFunction mExtendedFunction;
    private String mArguments;
    private Identifier mTargetAddress;
    private List<Identifier> mIdentifiers;

    /**
     * Constructs the message
     *
     * @param message containing the message bits
     * @param offset into the message for this structure
     */
    public ExtendedFunctionCommandAbbreviated(CorrectedBinaryMessage message, int offset)
    {
        super(message, offset);
    }

    /**
     * Textual representation of this message
     */
    public String toString()
    {
        StringBuilder sb = new StringBuilder();
        sb.append(getOpcode());
        sb.append(" TO:").append(getTargetAddress());
        sb.append(" FUNCTION:").append(getExtendedFunction());
        sb.append(getArguments());
        return sb.toString();
    }

    public ExtendedFunction getExtendedFunction()
    {
        if(mExtendedFunction == null)
        {
            mExtendedFunction = ExtendedFunction.fromValue(getInt(FUNCTION));
        }

        return mExtendedFunction;
    }

    public String getArguments()
    {
        if(mArguments == null)
        {
            mArguments = Integer.toHexString(getInt(ARGUMENTS)).toUpperCase();
        }

        return mArguments;
    }

    /**
     * To Talkgroup
     */
    public Identifier getTargetAddress()
    {
        if(mTargetAddress == null)
        {
            mTargetAddress = APCO25RadioIdentifier.createTo(getInt(TARGET_ADDRESS));
        }

        return mTargetAddress;
    }

    @Override
    public List<Identifier> getIdentifiers()
    {
        if(mIdentifiers == null)
        {
            mIdentifiers = Collections.singletonList(getTargetAddress());
        }

        return mIdentifiers;
    }
}
